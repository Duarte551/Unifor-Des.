class Lazer {
    public void parqueAquatico() {
        System.out.println("Você está no parque aquático. Divirta-se nas piscinas e toboáguas!");
    }

    public void passeioNaPraia() {
        System.out.println("Você está na praia. Aproveite o sol, o mar e a areia!");
    }

    public static void main(String[] args) {
        Lazer lazer = new Lazer();

        lazer.parqueAquatico();
        lazer.passeioNaPraia();
    }
}

